import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import nextEnv from "@next/env";

const { loadEnvConfig } = nextEnv;

const projectDirectory = path.resolve(
  path.dirname(fileURLToPath(import.meta.url)),
  "..",
);
const isDevelopment = process.env.NODE_ENV !== "production";

loadEnvConfig(projectDirectory, isDevelopment);

function parseAppUrl(value) {
  let url;

  try {
    url = new URL(value);
  } catch {
    throw new Error(`APP_URL이 올바른 URL이 아닙니다: ${value}`);
  }

  if (!["http:", "https:"].includes(url.protocol)) {
    throw new Error("APP_URL은 http 또는 https URL이어야 합니다.");
  }

  if (
    url.pathname !== "/" ||
    url.search ||
    url.hash ||
    url.username ||
    url.password
  ) {
    throw new Error("APP_URL에는 origin(프로토콜, 호스트, 포트)만 입력해 주세요.");
  }

  return url.origin;
}

const appUrl = parseAppUrl(process.env.APP_URL || "http://localhost:3000");
const extensionDirectory = path.join(projectDirectory, "chrome-extension");
const manifestTemplatePath = path.join(
  extensionDirectory,
  "manifest.template.json",
);
const manifestPath = path.join(extensionDirectory, "manifest.json");
const configPath = path.join(extensionDirectory, "config.js");
const appMatchPattern = `${appUrl}/*`;
const manifestTemplate = fs.readFileSync(manifestTemplatePath, "utf8");
const manifest = manifestTemplate.replaceAll(
  "__APP_MATCH_PATTERN__",
  appMatchPattern,
);
const config = `// Generated by scripts/sync-extension-config.mjs. Do not edit directly.
globalThis.SALTBREAD_CONFIG = Object.freeze({
  appUrl: ${JSON.stringify(appUrl)},
  apiBaseUrl: ${JSON.stringify(appUrl)},
  dashboardUrl: ${JSON.stringify(`${appUrl}/dashboard`)},
  detectPath: "/api/ext/detect",
  upbitApiBaseUrl: "https://api.upbit.com",
});
`;

JSON.parse(manifest);
fs.writeFileSync(manifestPath, manifest);
fs.writeFileSync(configPath, config);

console.log(`Chrome extension URL configured for ${appUrl}`);
